        s0 = Td0[(t0 >> 24)] ^ MACRO_ROTR(Td0[(t3 >> 16) & 0xff], 1) ^
             MACRO_ROTR(Td0[(t2 >> 8) & 0xff], 2) ^ MACRO_ROTR(Td0[(t1)&0xff], 3) ^ rk[0];
