    u32 s0, s1, s2, s3, t0, t1, t2, t3;

    int r;

    rk = key->rd_key;

    s0 = GETU32(in) ^ rk[0];
    s1 = GETU32(in + 4) ^ rk[1];
    s2 = GETU32(in + 8) ^ rk[2];
    s3 = GETU32(in + 12) ^ rk[3];

    r = key->rounds >> 1;
